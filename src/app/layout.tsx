import type { Metadata } from "next";
import { Poppins } from "next/font/google";
import "./globals.css";
import Navbar from "@/components/Navbar/Navbar";
import SessionProviders from "@/providers/SessionProviders";
import MusicPlayed from "@/components/MusicPlayed";
import { getServerSession } from "next-auth";
import { authOptions } from "./api/auth/[...nextauth]/route";

const poppins = Poppins({ weight: "400", subsets: ["latin"], preload: false });

export const metadata: Metadata = {
  title: "Spotify Clone",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const session = await getServerSession(authOptions);

  return (
    <html lang="en">
      <body className={poppins.className}>
        <main className="  bg container relative flex h-screen justify-center overflow-y-auto sm:h-[785px] sm:rounded-[20px] xl:w-[1280px]">
          {session && <Navbar />}
          {children}
          {session && <MusicPlayed />}
        </main>
      </body>
    </html>
  );
}
